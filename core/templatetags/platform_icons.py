PLATFORM_NAMES = {
    'telegram': 'Telegram',
    'vk': 'ВКонтакте',
    'max': 'MAX',
    'instagram': 'Instagram',
}

PLATFORM_BADGE_COLORS = {
    'telegram': ('e3f2fd', '0277bd'),
    'vk': ('e8eaf6', '3949ab'),
    'max': ('fff3e0', 'e65100'),
    'instagram': ('fce4ec', 'c62828'),
}


@register.simple_tag
def platform_icon(platform, size=14):
    """Возвращает SVG иконку платформы."""
    key = platform.lower()
    svg = PLATFORM_SVGS.get(key, f'<i class="bi bi-globe"></i>')
    return mark_safe(svg)


@register.simple_tag
def platform_badge(platform):
    """Возвращает полный цветной badge с иконкой и названием платформы."""
    key = platform.lower()
    icon = PLATFORM_SVGS.get(key, '')
    name = PLATFORM_NAMES.get(key, platform)
    bg, color = PLATFORM_BADGE_COLORS.get(key, ('f0f0f0', '333'))
    return mark_safe(
        f'<span style="display:inline-flex;align-items:center;gap:5px;background:#{bg};'
        f'color:#{color};padding:3px 10px;border-radius:20px;font-size:.75rem;font-weight:600;">'
        f'{icon} {name}</span>'
    )
